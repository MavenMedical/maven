    @Override
    public Detail getDetail() {
        HistoricProcedureDetail n = new HistoricProcedureDetail("");
        n.setNegative(HistoricProcedureNegationBox.isSelected());
        n.frameMin = 365*(int)HistoricProcedureTimeFrameYearSpinner.getValue();
        
        n.frameMin +=30*(int)HistoricProcedureTimeFrameMonthSpinner.getValue();
        
        n.frameMin +=(int)HistoricProcedureTimeFrameDaySpinner.getValue();
      
        n.frameMax = 365*(int)HistoricProcedureTimeFrameYearSpinner1.getValue();
       
        n.frameMax += 30*(int)HistoricProcedureTimeFrameMonthSpinner1.getValue();
        
        n.frameMax += (int)HistoricProcedureTimeFrameDaySpinner1.getValue();
     
        if (HistoricProcedureTimeFrameFutureBox.getSelectedItem().toString().equals("Before")){
            n.frameMin*=-1;
        }
        if (HistoricProcedureTimeFrameFutureBox1.getSelectedItem().toString().equals("Before")){
            n.frameMax*=-1;
        }
        n.CPT = Integer.parseInt(HistoricProcedureCPTField.getText());
        
        return n;
        
    }

   
