 public HistoricProcedure(MavenRuleFrontend myParentIn, HistoricProcedureDetail n) {
        myParent = myParentIn;
        editTarget = n;
       
        
        initComponents();
       
         int timeCalc = n.getMinDays();
         if(timeCalc>0){
             HistoricProcedureTimeFrameFutureBox.setSelectedIndex(1);
         } else {
             timeCalc *=-1;
         }
         HistoricProcedureTimeFrameYearSpinner.setValue(timeCalc/365);
        timeCalc%=365;
         HistoricProcedureTimeFrameMonthSpinner.setValue(timeCalc/30);
         timeCalc%=30;
         HistoricProcedureTimeFrameDaySpinner.setValue(timeCalc);
          timeCalc = n.getMaxDays();
          if(timeCalc>0){
             HistoricProcedureTimeFrameFutureBox1.setSelectedIndex(1);
         } else {
             timeCalc *=-1;
         }
        HistoricProcedureTimeFrameYearSpinner1.setValue(timeCalc/365);
        timeCalc%=365;
         HistoricProcedureTimeFrameMonthSpinner1.setValue(timeCalc/30);
         timeCalc%=30;
         HistoricProcedureTimeFrameDaySpinner1.setValue(timeCalc);
         HistoricProcedureCPTField.setText(n.CPT+"");
         HistoricProcedureNegationBox.setSelected(n.negative);
         
    }
